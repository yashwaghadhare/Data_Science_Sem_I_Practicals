# Input two numbers
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

# Relational operators
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)

# Logical operators
print("(a > 0) and (b > 0):", (a > 0) and (b > 0))
print("(a > 0) or (b > 0):", (a > 0) or (b > 0))
print("not(a > 0):", not(a > 0))
